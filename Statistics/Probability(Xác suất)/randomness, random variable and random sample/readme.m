Discrete random cariables 
1. A random variable is a variable whose value is a numerical outcome of a random phenomenon.
Example: A basketball player shoots three free throws. We define the random vaeiable X as the number of baskets successfully made.

2. A discrete random variable X has a finite number of possible values
Example: A basketball player shoots three free throws. The number of baskets successfully made is a discreta random variable (X). Xa can only take the values 0, 1, 2 or 3.

The probability distribution of a random variable X lists the values and their probabilities:
The probabilities p, must add up to 1.
Example: A basketball player shoots three free throws. The random variable X is the number of baskets successfully made.

The probability of any event is the sum of the probabilities p; of the values of X that make up the event.
Example: A basketball player shoots three free throws. The random variable X is the number of baskets successfully made.
Value of X  |0      1     2     3
------------|-------------------------
Probability |1/8    3/8   3/8   1/8
What is the probability that the player successfully makes at least two baskets ("at least two" means "two or more")?
P(X>=2) = P(X=2) + P(X=3) = 3/8 + 1/8 = 1/2
What is the probability that the player successfully makes fewer than three baskets?
P(X<3) = P(X=0) + P(X=1) + P(X=2) = 1/8 + 3/8 + 3/8 = 7/8 or
P(X<3) 1- P(X-3) = 1-1/8 = 7/8

